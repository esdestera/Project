        private async Task SignInAsync(ApplicationUser user, bool isPersistent)
        {
            AuthenticationManager.SignOut(DefaultAuthenticationTypes.ExternalCookie);

            var identity = await UserManager.CreateIdentityAsync(
               user, DefaultAuthenticationTypes.ApplicationCookie);

            AuthenticationManager.SignIn(
               new AuthenticationProperties()
               {
                   IsPersistent = isPersistent
               }, identity);
        }

        public void LoginTime(string userName)
        {
            var ip = GetIPAddress();
            using (var db = new ApplicationDbContext())
            {
                var user = db.Users.Where(m => m.Email == userName).FirstOrDefault();
                var model = new LoginHistory(DateTime.UtcNow, ip, user.Id);                
                db.LoginListory.Add(model);
                db.SaveChanges();
            }
        }

        public void LogOutTime(string userId)
        {
            using (var db = new ApplicationDbContext())
            {
                var model = db.LoginListory.Where(u => u.UserId == userId).OrderByDescending(u => u.LoginHistoryId).First();

                model.LogoutTime = DateTime.UtcNow;
                db.SaveChanges();
            }
        }

        public string GetIPAddress()
        {
            System.Web.HttpContext context = System.Web.HttpContext.Current;
            string ipAddress = context.Request.ServerVariables["HTTP_X_FORWARDED_FOR"];

            if (!string.IsNullOrEmpty(ipAddress))
            {
                string[] addresses = ipAddress.Split(',');
                if (addresses.Length != 0)
                {
                    return addresses[0];
                }
            }

            return context.Request.ServerVariables["REMOTE_ADDR"];
        }

